"use client";

import { motion } from "framer-motion";
import { Copy, Check, Sparkles, Layers, Code2 } from "lucide-react";
import { useState } from "react";
import { cn } from "@/lib/utils";

interface ResultData {
    summary: string;
    visualStyle: string;
    keySections: string[];
    techStack: string;
}

interface ResultCardProps {
    data: ResultData;
}

export function ResultCard({ data }: ResultCardProps) {
    const [copied, setCopied] = useState(false);

    const handleCopy = () => {
        const text = `
Summary: ${data.summary}
Visual Style: ${data.visualStyle}
Key Sections: ${data.keySections.join(", ")}
Tech Stack: ${data.techStack}
    `.trim();
        navigator.clipboard.writeText(text);
        setCopied(true);
        setTimeout(() => setCopied(false), 2000);
    };

    return (
        <motion.div
            initial={{ opacity: 0, y: 20 }}
            animate={{ opacity: 1, y: 0 }}
            exit={{ opacity: 0, scale: 0.95 }}
            transition={{ duration: 0.5, ease: "easeOut" }}
            className="w-full mt-8 bg-white/5 border border-white/10 backdrop-blur-md rounded-2xl overflow-hidden shadow-2xl shadow-indigo-500/10"
        >
            <div className="p-6 md:p-8 space-y-6">
                <div className="flex items-start justify-between gap-4">
                    <div>
                        <h3 className="text-xl font-semibold text-white/90">
                            Architectural Blueprint
                        </h3>
                        <p className="text-white/50 text-sm mt-1">Generated by Idea Alchemist</p>
                    </div>
                    <button
                        onClick={handleCopy}
                        className="p-2 text-white/40 hover:text-white hover:bg-white/10 rounded-lg transition-colors"
                        title="Copy to clipboard"
                    >
                        {copied ? <Check className="w-5 h-5 text-emerald-400" /> : <Copy className="w-5 h-5" />}
                    </button>
                </div>

                <div className="space-y-4">
                    <div className="space-y-2">
                        <h4 className="flex items-center text-sm font-medium text-indigo-300">
                            <Sparkles className="w-4 h-4 mr-2" /> Visual Style
                        </h4>
                        <p className="text-white/80 leading-relaxed">{data.visualStyle}</p>
                    </div>

                    <div className="h-px bg-white/5" />

                    <div className="space-y-2">
                        <h4 className="flex items-center text-sm font-medium text-pink-300">
                            <Layers className="w-4 h-4 mr-2" /> Key Sections
                        </h4>
                        <div className="flex flex-wrap gap-2">
                            {data.keySections.map((section, i) => (
                                <span
                                    key={i}
                                    className="px-3 py-1 bg-white/5 border border-white/10 rounded-full text-sm text-white/70"
                                >
                                    {section}
                                </span>
                            ))}
                        </div>
                    </div>

                    <div className="h-px bg-white/5" />

                    <div className="space-y-2">
                        <h4 className="flex items-center text-sm font-medium text-emerald-300">
                            <Code2 className="w-4 h-4 mr-2" /> Tech Stack
                        </h4>
                        <p className="text-white/80 font-mono text-sm">{data.techStack}</p>
                    </div>
                </div>
            </div>
            <div className="px-6 py-4 bg-white/5 border-t border-white/5">
                <p className="text-xs text-center text-white/30 truncate">
                    {data.summary}
                </p>
            </div>
        </motion.div>
    );
}
